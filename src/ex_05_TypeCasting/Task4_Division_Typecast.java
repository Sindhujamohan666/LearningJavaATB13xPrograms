package ex_05_TypeCasting;

public class Task4_Division_Typecast {
    public static void main(String[] args) {
        //Typecasting in Division
        //int a = 10, int b = 3; now divide 10/3 and store the
        // decimal value in the Result variable and print it.
        int a=10;
        int b=3;
        float result=a/b;
        System.out.println(result);
    }
}
